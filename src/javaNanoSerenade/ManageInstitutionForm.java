    private void jButtonEditInstitutionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditInstitutionActionPerformed
         // EDIT THE SELECTED CLIENT
        
        //get data from the fields
        int institutionID = 0;
        String department = jTextFieldDepartment.getText();
        String pinstitution = jTextFieldInstitution.getText();
        String city = jTextFieldCity.getText();
        String state = jTextFieldState.getText();
        String zipcode = jTextFieldZipcode.getText();
        String country = jTextFieldCountry.getText();
        
          if(department.trim().equals("") || pinstitution.trim().equals("") || city.trim().equals("") || state.trim().equals("") || zipcode.trim().equals("") || country.trim().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Required Fields -> First/Last Name + The Phone Number + The Email.", "Empty Fields", JOptionPane.WARNING_MESSAGE);
        }
        else {
              
              try{
                    institutionID = Integer.valueOf(jTextFieldInstitutionID.getText());
                    
                    if(institution.editInstitution(institutionID, department, pinstitution, city, state, zipcode, country))
                        {
                            JOptionPane.showMessageDialog(rootPane, "Institution Data Updated Successfully", "Edit Institution", JOptionPane.INFORMATION_MESSAGE);
                    }else{
                            JOptionPane.showMessageDialog(rootPane, "Institution Data Not Updated ", "Edit Institution Error", JOptionPane.ERROR_MESSAGE);
                        }
               }
              catch(NumberFormatException ex)
              {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage() + "Enter The Institution Id (number)", "Institution ID Error", JOptionPane.ERROR_MESSAGE);
            }
                      
            
        }
          //clear the JTable first
        jTable1.setModel(new DefaultTableModel(null, new Object[]{"Institution ID", "Department", "Institution", "City", "State", "Zipcode", "Country"}));
        
        // then populate the JTable
        institution.fillInstitutionJTable(jTable1);
    }//GEN-LAST:event_jButtonEditInstitutionActionPerformed

