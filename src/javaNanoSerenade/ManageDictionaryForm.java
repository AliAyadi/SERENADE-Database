    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        //get data from the fields
        int dictionaryID = 0;
        String group = jTextFieldGroup.getText();
        String item = jTextFieldItem.getText();
        String parameter = jTextFieldParameter.getText();
        
          if(group.trim().equals("") || item.trim().equals("") || parameter.trim().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Required Fields -> Group + Item + Parameter.", "Empty Fields", JOptionPane.WARNING_MESSAGE);
        }
        else {
              
              try{
                    dictionaryID = Integer.valueOf(jTextFieldDictionaryID.getText());
                    
                    if(dictionary.editDictionary(dictionaryID, group, item, parameter))
                        {
                            JOptionPane.showMessageDialog(rootPane, "Dictionary Data Updated Successfully", "Edit Dictionary", JOptionPane.INFORMATION_MESSAGE);
                    }else{
                            JOptionPane.showMessageDialog(rootPane, "Dictionary Data Not Updated ", "Edit Dictionary Error", JOptionPane.ERROR_MESSAGE);
                        }
               }
              catch(NumberFormatException ex)
              {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage() + "Enter The Dictionary Id ", "Dictionary ID Error", JOptionPane.ERROR_MESSAGE);
            }
                      
            
        }
          //clear the JTable first
       jTable1.setModel(new DefaultTableModel(null, new Object[]{"Dictionary ID", "Group", "Item", "Parameter"}));
       
        // then populate the JTable
        dictionary.fillDictionaryJTable(jTable1);
    }//GEN-LAST:event_jButton10ActionPerformed

    private void jButtonRemoveDictionaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveDictionaryActionPerformed
         // Delete the selected client
        try{
                    int dictionaryID = Integer.valueOf(jTextFieldDictionaryID.getText());
                    
                    if(dictionary.removeDictionary(dictionaryID))
                        {
                            JOptionPane.showMessageDialog(rootPane, "Dictionary Deleted Successfully", "Remove Dictionary", JOptionPane.INFORMATION_MESSAGE);
                    }
                    else{
                            JOptionPane.showMessageDialog(rootPane, "Dictionary Not Deleted ", "Remove Dictionary Error", JOptionPane.ERROR_MESSAGE);
                        }
               }
              catch(NumberFormatException ex)
              {
                JOptionPane.showMessageDialog(rootPane, ex.getMessage() + "Enter The Dictionary Id", "Dictionary ID Error", JOptionPane.ERROR_MESSAGE);
            }
        //clear the JTable first
       jTable1.setModel(new DefaultTableModel(null, new Object[]{"Dictionary ID", "Group", "Item", "Parameter"}));
       
        // then populate the JTable
        dictionary.fillDictionaryJTable(jTable1);
    }//GEN-LAST:event_jButtonRemoveDictionaryActionPerformed

