    private void jButtonAddDictionaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddDictionaryActionPerformed
          //add a new cient
        
        //get data from the fields
        String group = jTextFieldGroup.getText();
        String item = jTextFieldItem.getText();
        String parameter = jTextFieldParameter.getText();
        
        
        if(group.trim().equals("") || item.trim().equals("") || parameter.trim().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Required Fields -> Group + Item + Parameter.", "Empty Fields", JOptionPane.WARNING_MESSAGE);
        }
        else {
            if(dictionary.addDictionary(group, item, parameter))
            {
                JOptionPane.showMessageDialog(rootPane, "New Dictionary added Successfully", "Add Dictionary", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Dictionary Not Added ", "Add Dictionary Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        //clear the JTable first
       jTable1.setModel(new DefaultTableModel(null, new Object[]{"Dictionary ID", "Group", "Item", "Parameter"}));
       
        // then populate the JTable
        dictionary.fillDictionaryJTable(jTable1);
    }//GEN-LAST:event_jButtonAddDictionaryActionPerformed
 private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {                                     
        //display the selected row data in the jtextfields
        
        //get the jlabel model 
        DefaultTableModel model = (DefaultTableModel )jTable1.getModel();
                
        // get the selected row index
        int rIndex = jTable1.getSelectedRow();
        
        //display data 
        jTextFieldDictionaryID.setText(model.getValueAt(rIndex, 0).toString());
        jTextFieldGroup.setText(model.getValueAt(rIndex, 1).toString());
        jTextFieldItem.setText(model.getValueAt(rIndex, 2).toString());
        jTextFieldParameter.setText(model.getValueAt(rIndex, 3).toString());
    }                              
 
