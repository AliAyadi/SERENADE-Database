    private void jButtonAddDescriptionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddDescriptionActionPerformed
         //add a new cient
        
        //get data from the fields
        String table = jTextFieldTable.getText();
        String field = jTextFieldField.getText();
        String note = jTextFieldNote.getText();
        String data_type = jTextFieldDataType.getText();
        String data_value = jTextFieldValidValue.getText();
        
        if(table.trim().equals("") || field.trim().equals("") || note.trim().equals("") || data_type.trim().equals("") || data_value.trim().equals(""))
        {
            JOptionPane.showMessageDialog(rootPane, "Required Fields -> Table + Field + Data type + Data value.", "Empty Fields", JOptionPane.WARNING_MESSAGE);
        }
        else {
            if(description.addDescription(table, field, note, data_type, data_value))
            {
                JOptionPane.showMessageDialog(rootPane, "New Description added Successfully", "Add Client", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Description Not Added ", "Add Client Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        //clear the JTable first
       jTable1.setModel(new DefaultTableModel(null, new Object[]{"Descrion ID", "Table", "Field", "Note", "Data type", "Data value"}));
       
        // then populate the JTable
        description.fillDescriptionJTable(jTable1);
    }//GEN-LAST:event_jButtonAddDescriptionActionPerformed

    
    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {                                     
        //display the selected row data in the jtextfields
        
        //get the jlabel model 
        DefaultTableModel model = (DefaultTableModel )jTable1.getModel();
                
        // get the selected row index
        int rIndex = jTable1.getSelectedRow();
        
        //display data 
        jTextFieldDescriptionID.setText(model.getValueAt(rIndex, 0).toString());
        jTextFieldTable.setText(model.getValueAt(rIndex, 1).toString());
        jTextFieldField.setText(model.getValueAt(rIndex, 2).toString());
        jTextFieldNote.setText(model.getValueAt(rIndex, 3).toString());
        jTextFieldDataType.setText(model.getValueAt(rIndex, 4).toString());
        jTextFieldValidValue.setText(model.getValueAt(rIndex, 5).toString());
    }                              
    
