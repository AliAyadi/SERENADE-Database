    private void jButtonAddBibliographyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddBibliographyActionPerformed
          //add a new cient
        
        //get data from the fields
        try{
            int peopleID = Integer.parseInt(jTextFieldPeopleID.getText());
            int institutionID = Integer.parseInt(jTextFieldInstitutionID.getText());
            String doi = jTextFieldIDOI.getText();
        
            if(bibliography.addBibliography(peopleID, institutionID, doi))
            {
                JOptionPane.showMessageDialog(rootPane, "New Bibliography added Successfully", "Add Bibliography", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(rootPane, "Bibliography Not Added ", "Add Bibliography Error", JOptionPane.ERROR_MESSAGE);
            }
            } catch(NumberFormatException ex)
                    {
                       JOptionPane.showMessageDialog(rootPane, "Required Fields -> People ID + Institution ID + DOI.", "Empty Fields", JOptionPane.WARNING_MESSAGE);
                    }
        //clear the JTable first
       jTable1.setModel(new DefaultTableModel(null, new Object[]{"Bibliography ID", "People ID", "Institution ID", "DOI"}));
       
        // then populate the JTable
        bibliography.fillBibliographyJTable(jTable1);
                       
    }//GEN-LAST:event_jButtonAddBibliographyActionPerformed
 private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {                                     
        //display the selected row data in the jtextfields
        
        //get the jlabel model 
        DefaultTableModel model = (DefaultTableModel )jTable1.getModel();
                
        // get the selected row index
        int rIndex = jTable1.getSelectedRow();
        
        //display data 
        jTextFieldBibliographyID.setText(model.getValueAt(rIndex, 0).toString());
        jTextFieldPeopleID.setText(model.getValueAt(rIndex, 1).toString());
        jTextFieldInstitutionID.setText(model.getValueAt(rIndex, 2).toString());
        jTextFieldIDOI.setText(model.getValueAt(rIndex, 3).toString());
    }                              
 
